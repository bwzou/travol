package po;

public class Rating {
	private int server;
	private int travel;
	private int website;
	public int getServer() {
		return server;
	}
	public void setServer(int server) {
		this.server = server;
	}
	public int getTravel() {
		return travel;
	}
	public void setTravel(int travel) {
		this.travel = travel;
	}
	public int getWebsite() {
		return website;
	}
	public void setWebsite(int website) {
		this.website = website;
	}
}
